public class Pitagoras {
    
    public static double calcularhipotenusa(double catetoa, double catetob){
        double hipotenusa;
        hipotenusa = Math.sqrt((catetoa*catetoa) + (catetob*catetob));
        return hipotenusa;
    }
    
}
